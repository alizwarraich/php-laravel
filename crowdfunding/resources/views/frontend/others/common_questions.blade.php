<div>
	<h4 style="padding-bottom: 20px; padding-top: 20px;">How does FundOurCause work?</h4>
	<p>You start your FundOurCause by telling your story and setting a goal. You will then receive a fundraising page to accept donations and share your fundraiser. Signing up is easy and every donation is yours to keep, whether or not you reach your goal.
FundOurCause makes it incredibly easy to raise money online for the things that matter to you most. In just a few minutes, you’ll be able to personalize your fundraiser and share it with the people in your life. In fact, over $9 billion has been raised by individuals on FundOurCause.
Raising money for yourself or a loved one has never been easier. Plus, we’re here to help. Our Customer Care team will support you with customer service every step of the way.</p>
	<hr style="border-top: 2px solid #bbb;">
	<h4 style="padding-bottom: 20px; padding-top: 20px;">How does FundOurCause keep fundraisers safe?</h4>

	<p>FundOurCause features the very best in secure payment encryption technology. Your donors’ online payments are safe, and your money is stored securely until you’re ready to request a withdrawal via electronic bank transfer.</p>
	<hr style="border-top: 2px solid #bbb;">
	<h4 style="padding-bottom: 20px; padding-top: 20px;">What can i raise money for?</h4>
	<p>We see people use FundOurCause to raise money for themselves, friends and family, or even complete strangers in random acts of kindness. People raise money for just about everything, including medical expenses, education costs, volunteer programs, youth sports, funerals & memorials, and even animals & pets.

We’re always amazed at the ways people use FundOurCause to raise money. Check out some recent success stories, or see how FundOurCause works.</p>

	<hr style="border-top: 2px solid #bbb;">
	<h4 style="padding-bottom: 20px; padding-top: 20px;">How can i withdraw money from my fundraiser?</h4>
	<p>Once your FundOurCause starts receiving online donations, you can easily request a withdrawal at any time. Withdrawing money does not affect the progress meter displayed on your fundraiser. Simply click ‘Withdraw’ while logged into your account and follow the instructions. You can withdraw your balance directly to your bank account. Bank transfers take 2-5 business days to arrive.</p>

	<hr style="border-top: 2px solid #bbb;">
	<h4 style="padding-bottom: 20px; padding-top: 20px;">Can a friend withdraw the money i raise for them?</h4>
	<p>Yes. FundOurCause makes it easy for another friend or family member to securely access the funds you have raised. Through FundOurCause, they will receive direct access to the money you have raised. Please note: You will not be able to enter their bank information during the withdrawal process; they will need to do this themselves.</p>

	<hr style="border-top: 2px solid #bbb;">
	<h4 style="padding-bottom: 20px; padding-top: 20px;">Are there any deadlines or time limits?</h4>
    <p>While there are important withdrawals deadlines you’ll need to observe to avoid donations being refunded to donors, your fundraiser will remain live until you choose to turn off donations or remove the fundraiser altogether. Most organizers leave their fundraisers active indefinitely to refer back to the kind comments and support they received.</p>
	

	<hr style="border-top: 2px solid #bbb;">
	<h4 style="padding-bottom: 20px; padding-top: 20px;">What if i don't reach my goal?</h4>
	<p>No problem. Reaching your goal is not required. With FundOurCause, you keep each and every donation you receive. Your fundraiser will be able to accept donations even after your goal is reached. Once the goal is reached, the progress meter on your fundraiser will show that you have received more than your goal amount. If you’d like to continue raising money, you can keep your fundraiser running for as long as you’d like.</p>

	<hr style="border-top: 2px solid #bbb;">
	<h4 style="padding-bottom: 20px; padding-top: 20px;">How do i get donations?</h4>
	<p>FundOurCause provides an easy way to raise money from your friends, family, and online community. Our platform makes it simple to share your fundraiser in a variety of ways to bring in donations, track your progress, and post updates to engage your community. Check out our fundraising tips and fundraising ideas for advice on how to raise more donations.</p>
	<hr style="border-top: 2px solid #bbb;">
